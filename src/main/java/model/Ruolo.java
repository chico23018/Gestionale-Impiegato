package model;

public class Ruolo {
	
	private int idRuollo;
	private String discrizione;
	
	
	public int getIdRuollo() {
		return idRuollo;
	}
	public void setIdRuollo(int idRuollo) {
		this.idRuollo = idRuollo;
	}
	public String getDiscrizione() {
		return discrizione;
	}
	public void setDiscrizione(String discrizione) {
		this.discrizione = discrizione;
	}
	

	

}
